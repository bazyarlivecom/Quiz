// ุชุงุจุน ุจุฑุง ุงุฌุงุฏ ุณูุงูุงุช
async function generateQuestions() {
  const client = new Client(config);

  try {
    console.log('Connecting to database...');
    await client.connect();
    console.log('Connected successfully!');

    // ุฏุฑุงูุช ุฏุณุชูโุจูุฏโูุง
    const categoriesResult = await client.query('SELECT id, name FROM categories WHERE is_active = true');
    const categories = categoriesResult.rows;
    
    console.log(`Found ${categories.length} categories`);

    let totalQuestions = 0;

    // ุจุฑุง ูุฑ ุฏุณุชูโุจูุฏ
    for (const category of categories) {
      const categoryName = category.name;
      const categoryId = category.id;
      const questions = questionsByCategory[categoryName] || [];

      if (questions.length === 0) {
        console.log(`No questions defined for category: ${categoryName}`);
        continue;
      }

      console.log(`\nProcessing category: ${categoryName} (ID: ${categoryId})`);

      // ุงุฌุงุฏ ุณูุงูุงุช ุจุฑุง ุงู ุฏุณุชูโุจูุฏ
      for (let i = 0; i < questions.length; i++) {
        const q = questions[i];
        
        try {
          // ุจุฑุฑุณ ูุฌูุฏ ุณูุงู ูุดุงุจู
          const existingQuestion = await client.query(
            'SELECT id FROM questions WHERE question_text = $1 AND category_id = $2',
            [q.question, categoryId]
          );

          if (existingQuestion.rows.length > 0) {
            console.log(`  Question ${i + 1} already exists, skipping...`);
            continue;
          }

          // ุงุฌุงุฏ ุณูุงู
          const questionResult = await client.query(
            `INSERT INTO questions (category_id, difficulty, question_text, explanation, points, is_active)
             VALUES ($1, $2, $3, $4, $5, $6)
             RETURNING id`,
            [categoryId, q.difficulty, q.question, q.explanation || null, q.points || 10, true]
          );

          const questionId = questionResult.rows[0].id;

          // ุงุฌุงุฏ ฺฏุฒููโูุง
          for (let j = 0; j < q.options.length; j++) {
            await client.query(
              `INSERT INTO question_options (question_id, option_text, is_correct, option_order)
               VALUES ($1, $2, $3, $4)`,
              [questionId, q.options[j], j === q.correct, j + 1]
            );
          }

          totalQuestions++;
          console.log(`  โ Created question ${i + 1}: ${q.question.substring(0, 50)}...`);
        } catch (error) {
          console.error(`  โ Error creating question ${i + 1}:`, error.message);
        }
      }
    }

    console.log(`\nโ Successfully created ${totalQuestions} questions!`);
    
    // ููุงุด ุขูุงุฑ
    const statsResult = await client.query(`
      SELECT 
        c.name as category,
        q.difficulty,
        COUNT(*) as count
      FROM questions q
      JOIN categories c ON q.category_id = c.id
      WHERE q.is_active = true
      GROUP BY c.name, q.difficulty
      ORDER BY c.name, q.difficulty
    `);

    console.log('\n๐ Statistics:');
    console.log('Category | Difficulty | Count');
    console.log('---------|------------|------');
    statsResult.rows.forEach(row => {
      console.log(`${row.category.padEnd(8)} | ${row.difficulty.padEnd(10)} | ${row.count}`);
    });

    const totalResult = await client.query('SELECT COUNT(*) as total FROM questions WHERE is_active = true');
    console.log(`\nTotal active questions: ${totalResult.rows[0].total}`);

  } catch (error) {
    console.error('\nโ Error:', error.message);
    console.error('\nPlease check:');
    console.error('1. PostgreSQL is running');
    console.error('2. Database "quiz_game" exists');
    console.error('3. Tables are created');
    process.exit(1);
  } finally {
    await client.end();
    console.log('\nโ Database connection closed.');
    process.exit(0);
  }
}

